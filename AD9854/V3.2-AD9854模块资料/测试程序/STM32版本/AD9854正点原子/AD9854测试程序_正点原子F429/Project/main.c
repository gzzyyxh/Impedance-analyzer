// 头文件包含
//-----------------------------------------------------------------
#include "stm32f429_winner.h"
#include "delay.h"
#include "led.h"
#include "AD9854.h"
//-----------------------------------------------------------------

//-----------------------------------------------------------------
// 主程序
//-----------------------------------------------------------------
int main(void)
{
	u32 Fre;  
	delay_ms(100);	
	GPIO_AD9854_Configuration();
	delay_ms(5);
	AD9854_Init ();
	delay_ms(15);
	Fre = 1000;
	if (Fre > 99999999)
	{
		Fre = 99999999;
	}
	AD9854_SetSine ( Fre , 4095 );
	while (1)
	{
		;
	}
}

