#include <c8051f020.h>
#include "system_init.h"
#include "AD9854.h"
#include "CharLCD.h"
#include "delay.h"
#include "type.h"

//-----------------------------------------------------------------
//               IO 连接说明
//-----------------------------------------------------------------
//    AD9854         |      020         
// ADDER_BUS[5:0]    -->   P1{5:0}
// DATA_BUS[7:0]     -->   P2{7:0}
// RD                -->   P07
// WR                -->   P06
// UDCLK             -->   P05
// RESET             -->   P04
// S/P               -->   P00
// OSK               -->   P01
// FDSTA             -->   P02
//-----------------------------------------------------------------

//-----------------------------------------------------------------
// 主程序
//-----------------------------------------------------------------
void main(void)
{  
	ulong fre ;
	
	init();
	WrCLcdC(0x06);
	WriteString(1, 1,"  AD9854 TEST!  ");
	WriteString(2, 1,"Fre:         Hz");
  AD9854_Init();
	
	fre = 1000000;
	if (fre > 99999999)
	{
		fre = 99999999;
	}
	WrCLcd_long_num (2,6,fre);
	AD9854_SetSine ( fre , 4095 );
  while (1)
  {
    ;							 				
  }
}

